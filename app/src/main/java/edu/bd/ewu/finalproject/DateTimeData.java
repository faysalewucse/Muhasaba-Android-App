package edu.bd.ewu.finalproject;

public class DateTimeData {
    String fajr, sunrise, dhuhr, asr, maghrib, isha, sunset, hijri_date;

    public DateTimeData(String fajr, String sunrise, String dhuhr, String asr, String maghrib, String isha, String sunset, String hijri_date) {
        this.fajr = fajr;
        this.sunrise = sunrise;
        this.dhuhr = dhuhr;
        this.asr = asr;
        this.maghrib = maghrib;
        this.isha = isha;
        this.sunset = sunset;
        this.hijri_date = hijri_date;
    }

    public String getFajr() {
        return fajr;
    }

    public void setFajr(String fajr) {
        this.fajr = fajr;
    }

    public String getSunrise() {
        return sunrise;
    }

    public void setSunrise(String sunrise) {
        this.sunrise = sunrise;
    }

    public String getDhuhr() {
        return dhuhr;
    }

    public void setDhuhr(String dhuhr) {
        this.dhuhr = dhuhr;
    }

    public String getAsr() {
        return asr;
    }

    public void setAsr(String asr) {
        this.asr = asr;
    }

    public String getMaghrib() {
        return maghrib;
    }

    public void setMaghrib(String maghrib) {
        this.maghrib = maghrib;
    }

    public String getIsha() {
        return isha;
    }

    public void setIsha(String isha) {
        this.isha = isha;
    }

    public String getSunset() {
        return sunset;
    }

    public void setSunset(String sunset) {
        this.sunset = sunset;
    }

    public String getHijri_date() {
        return hijri_date;
    }

    public void setHijri_date(String hijri_date) {
        this.hijri_date = hijri_date;
    }
}
